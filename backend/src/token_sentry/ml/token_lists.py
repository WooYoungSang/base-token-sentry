def get_label_hint(address: str) -> str | None:
    """Return a label hint for a known address, or None if unknown."""
    addr = address.lower()
    for addr_map, label in [
        (SAFE_TOKENS, "safe"),
        (KNOWN_SCAMS, "scam"),
    ]:
        for known_addr in addr_map:
            if known_addr.lower() == addr:
                return label
    return None
